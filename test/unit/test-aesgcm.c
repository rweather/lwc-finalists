static void test_aesgcm_vector
    (const aead_cipher_t *cipher, const TestVector *test)
{
    unsigned char ciphertext[MAX_PLAINTEXT_LEN + AESGCM_TAG_SIZE];
    unsigned char plaintext[MAX_PLAINTEXT_LEN];
    size_t clen, mlen;
    int ok;

    printf("    %s ... ", test->name);
    fflush(stdout);

    memset(ciphertext, 0xAA, sizeof(ciphertext));
    memset(plaintext, 0xBB, sizeof(plaintext));

    (*cipher->encrypt)(ciphertext, &clen, test->plaintext, test->datasize,
                       test->authdata, test->authsize, test->iv, test->key);
    ok = test_memcmp(ciphertext, test->ciphertext, test->datasize) == 0;
    ok &= test_memcmp(ciphertext + test->datasize,
                      test->tag, test->tagsize) == 0;

    (*cipher->decrypt)(plaintext, &mlen, ciphertext,
                       test->datasize + test->tagsize,
                       test->authdata, test->authsize,
                       test->iv, test->key);
    ok = test_memcmp(plaintext, test->plaintext, test->datasize) == 0;

    if (ok) {
        printf("ok\n");
    } else {
        printf("failed\n");
        test_exit_result = 1;
    }
}

void test_aesgcm(void)
{
    printf("AES-GCM:\n");

    test_aesgcm_vector(&internal_aesgcm128_cipher, &testVectorGCM1);
    test_aesgcm_vector(&internal_aesgcm128_cipher, &testVectorGCM2);
    test_aesgcm_vector(&internal_aesgcm128_cipher, &testVectorGCM3);
    test_aesgcm_vector(&internal_aesgcm128_cipher, &testVectorGCM4);
    test_aesgcm_vector(&internal_aesgcm192_cipher, &testVectorGCM10);
    test_aesgcm_vector(&internal_aesgcm256_cipher, &testVectorGCM16);

    printf("\n");
}
