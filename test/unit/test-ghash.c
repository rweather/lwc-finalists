static void test_ghash_vector(const TestVector *test)
{
    ghash_state_t state;
    unsigned char actual[16];

    printf("    %s ... ", test->name);
    fflush(stdout);

    ghash_init(&state, test->key);
    ghash_update(&state, test->data, test->dataLen);
    ghash_finalize(&state, actual);

    if (test_memcmp(actual, test->hash, 16) != 0) {
        printf("failed\n");
        test_exit_result = 1;
    } else {
        printf("ok\n");
    }
}

void test_ghash(void)
{
    printf("GHASH:\n");

    test_ghash_vector(&testVectorGHASH_1);
    test_ghash_vector(&testVectorGHASH_2);
    test_ghash_vector(&testVectorGHASH_3);
    test_ghash_vector(&testVectorGHASH_4);

    printf("\n");
}
