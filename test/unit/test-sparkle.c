void test_sparkle(void)
{
    uint32_t state256[SPARKLE_256_STATE_SIZE];
    uint32_t state384[SPARKLE_384_STATE_SIZE];
    uint32_t state512[SPARKLE_512_STATE_SIZE];

    printf("SPARKLE Permutation:\n");

    printf("    SPARKLE-256 ... ");
    fflush(stdout);
    memcpy(state256, sparkle256_input, sizeof(sparkle256_input));
    sparkle_256(state256, 10);
    if (!test_memcmp((unsigned char *)state256, sparkle256_output, sizeof(sparkle256_output))) {
        printf("ok\n");
    } else {
        printf("failed\n");
        test_exit_result = 1;
    }

    printf("    SPARKLE-384 ... ");
    fflush(stdout);
    memcpy(state384, sparkle384_input, sizeof(sparkle384_input));
    sparkle_384(state384, 11);
    if (!test_memcmp((unsigned char *)state384, sparkle384_output, sizeof(sparkle384_output))) {
        printf("ok\n");
    } else {
        printf("failed\n");
        test_exit_result = 1;
    }

    printf("    SPARKLE-512 ... ");
    fflush(stdout);
    memcpy(state512, sparkle512_input, sizeof(sparkle512_input));
    sparkle_512(state512, 12);
    if (!test_memcmp((unsigned char *)state512, sparkle512_output, sizeof(sparkle512_output))) {
        printf("ok\n");
    } else {
        printf("failed\n");
        test_exit_result = 1;
    }

    printf("\n");
}
